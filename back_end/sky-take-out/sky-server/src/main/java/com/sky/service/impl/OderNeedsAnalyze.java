package com.sky.service.impl;

/**
 * =========================
 * 需求分析和设计
 * =========================
 * 设计：
 * 1) 通过 WebSocket 实现管理端页面和服务端保持长连接状态
 * 2) 当客户支付后，调用 WebSocket 的相关 API，实现服务端向客户端推送消息
 * 3) 客户端浏览器解析服务端推送的消息，判断是来单提醒还是客户催单，并进行相应的消息提示和语音播报
 * 4) 约定服务端发送给客户端浏览器的数据格式为 JSON，字段包括：type、orderId、content
 *    - type：消息类型，1 为来单提醒，2 为客户催单
 *    - orderId：订单 id
 *    - content：消息内容
 */